#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		aux;

	aux = 0;
	i = 0;
	while (str[i] != '\0')
	{
		aux *= 10;
		aux += str[i] - '0';
		i++;
	}
	return (aux);
}
